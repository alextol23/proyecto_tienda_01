    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        try{
            con = getConection();
            ps = con.prepareStatement("SELECT * FROM empleado WHERE (id_empleado)= ?");
            ps.setInt(1,Integer.parseInt(txtid_empl.getText()));

            rs = ps.executeQuery();
            if(rs.next()){
                txtid_empl.setText(rs.getString("id_empleado"));
                txtnombre.setText(rs.getString("nombre"));
                txtapellidos.setText(rs.getString("apellidos"));
                txtsueldo.setText(rs.getString("sueldo"));
                txtcargo.setText(rs.getString("cargo"));
                txtempresa.setText(rs.getString("empresa"));
            }
        }catch(Exception e){
            System.err.println(e);
        }

    }//GEN-LAST:event_buscarActionPerformed

