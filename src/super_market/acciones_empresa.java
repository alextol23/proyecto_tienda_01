    private void modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        try{
            con = getConection();
            ps = con.prepareStatement("UPDATE empleado SET id_empleado=?, nombre=?, apellidos=?, sueldo=?, cargo=?, empresa=? WHERE id_empleado=? ");
            ps.setInt(1,Integer.parseInt(txtid_empl.getText()));
            ps.setString(2,txtnombre.getText());
            ps.setString(3,txtapellidos.getText());
            ps.setInt(4,Integer.parseInt(txtsueldo.getText()));
            ps.setInt(5,Integer.parseInt(txtcargo.getText()));
            ps.setInt(6,Integer.parseInt(txtempresa.getText()));
            ps.setInt(7,Integer.parseInt(txtid_empl.getText()));

            int res = ps.executeUpdate();
            if (res >0){
                JOptionPane.showMessageDialog(null, "persona modificada");
            }else{
                JOptionPane.showMessageDialog(null, "error al modificar");
            }
            con.close();
        }catch(Exception e){
            System.err.println(e);
        }
    }//GEN-LAST:event_modificarActionPerformed

